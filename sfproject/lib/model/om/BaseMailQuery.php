 * @method Mail findOneByIsDeleted(int $is_deleted) Return the first Mail filtered by the is_deleted column
 * @method Mail findOneByUpdatedAt(string $updated_at) Return the first Mail filtered by the updated_at column
 * @method Mail findOneByDeletedAt(string $deleted_at) Return the first Mail filtered by the deleted_at column
 * @method Mail findOneByCreatedAt(string $created_at) Return the first Mail filtered by the created_at column
 * @method Mail findOneByTitle(string $title) Return the first Mail filtered by the title column
 * @method Mail findOneByText(string $text) Return the first Mail filtered by the text column
 *
 * @method array findById(int $id) Return Mail objects filtered by the id column
 * @method array findByIsDeleted(int $is_deleted) Return Mail objects filtered by the is_deleted column
 * @method array findByUpdatedAt(string $updated_at) Return Mail objects filtered by the updated_at column
 * @method array findByDeletedAt(string $deleted_at) Return Mail objects filtered by the deleted_at column
 * @method array findByCreatedAt(string $created_at) Return Mail objects filtered by the created_at column
 * @method array findByTitle(string $title) Return Mail objects filtered by the title column
 * @method array findByText(string $text) Return Mail objects filtered by the text column
 *
 * @package    propel.generator.lib.model.om

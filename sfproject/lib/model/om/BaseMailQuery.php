/**
 * Base class that represents a query for the 'mail' table.
 *
 *
 *
 * @method MailQuery orderById($order = Criteria::ASC) Order by the id column
 * @method MailQuery orderByIsDeleted($order = Criteria::ASC) Order by the is_deleted column
 * @method MailQuery orderByUpdatedAt($order = Criteria::ASC) Order by the updated_at column
 * @method MailQuery orderByDeletedAt($order = Criteria::ASC) Order by the deleted_at column
 * @method MailQuery orderByCreatedAt($order = Criteria::ASC) Order by the created_at column
 * @method MailQuery orderByTitle($order = Criteria::ASC) Order by the title column
 * @method MailQuery orderByText($order = Criteria::ASC) Order by the text column
 *
 * @method MailQuery groupById() Group by the id column
 * @method MailQuery groupByIsDeleted() Group by the is_deleted column
 * @method MailQuery groupByUpdatedAt() Group by the updated_at column
 * @method MailQuery groupByDeletedAt() Group by the deleted_at column
 * @method MailQuery groupByCreatedAt() Group by the created_at column
 * @method MailQuery groupByTitle() Group by the title column
 * @method MailQuery groupByText() Group by the text column
 *
 * @method MailQuery leftJoin($relation) Adds a LEFT JOIN clause to the query
 * @method MailQuery rightJoin($relation) Adds a RIGHT JOIN clause to the query
 * @method MailQuery innerJoin($relation) Adds a INNER JOIN clause to the query
 *
 * @method Mail findOne(PropelPDO $con = null) Return the first Mail matching the query
 * @method Mail findOneOrCreate(PropelPDO $con = null) Return the first Mail matching the query, or a new Mail object populated from the query conditions when no match is found
